let currentAccount, logOutTimer;

// Login button click event handling.
btnLogin.addEventListener('click', function (e) {
  // Prevent form from submitting.
  e.preventDefault();

  // Check if username exist in data.
  currentAccount = accounts.find(
    acc => acc.username === inputLoginUsername.value
  );

  // Log In if user exists and pin is OK.
  if (currentAccount?.pin === Number(inputLoginPin.value)) {
    // Display UI and welcome message.
    labelWelcome.textContent = `Welcome back, ${
      currentAccount.owner.split(' ')[0]
    }`;
    containerApp.style.opacity = 100;

    // Date formatting options.
    const currentDate = new Date();
    const dateOptions = {
      hour: 'numeric',
      minute: 'numeric',
      day: 'numeric',
      month: 'numeric',
      year: 'numeric',
    };

    // Display current date formatted using dateOptions object and current account
    // ISO language code.
    labelDate.textContent = new Intl.DateTimeFormat(
      currentAccount.locale,
      dateOptions
    ).format(currentDate);

    // Clear input fields when logged in.
    inputLoginUsername.value = inputLoginPin.value = '';
    inputLoginPin.blur();

    // If timer is already running reset it.
    // Then initialize it.
    if (logOutTimer) clearInterval(logOutTimer);
    logOutTimer = startLogOutTimer();

    // Update UI
    updateUI(currentAccount);
  }
});

// Transfer button click event handling.
btnTransfer.addEventListener('click', function (e) {
  e.preventDefault();
  const amount = +inputTransferAmount.value;
  // Check if receiver username exists.
  const receiverAcc = accounts.find(
    acc => acc.username === inputTransferTo.value
  );
  inputTransferAmount.value = inputTransferTo.value = '';

  if (
    amount > 0 &&
    receiverAcc &&
    currentAccount.balance >= amount &&
    receiverAcc?.username !== currentAccount.username
  ) {
    // Doing the transfer
    currentAccount.movements.push(-amount);
    receiverAcc.movements.push(amount);
    currentAccount.movementsDates.push(new Date().toISOString());
    receiverAcc.movementsDates.push(new Date().toISOString());
    updateUI(currentAccount);

    // Reset Log Out Timer.
    clearInterval(logOutTimer);
    timer = startLogOutTimer();
  }
});

// Loan button click event handling.
btnLoan.addEventListener('click', function (e) {
  e.preventDefault();

  const amount = Math.floor(inputLoanAmount.value);

  if (amount > 0 && currentAccount.movements.some(mov => mov >= amount * 0.1)) {
    currentAccount.movements.push(amount);
    currentAccount.movementsDates.push(new Date().toISOString());
    updateUI(currentAccount);

    // Reset Log Out Timer.
    clearInterval(logOutTimer);
    timer = startLogOutTimer();
  }
  inputLoanAmount.value = '';
});

// Close account button click event handling.
btnClose.addEventListener('click', function (e) {
  e.preventDefault();

  if (
    inputCloseUsername.value === currentAccount.username &&
    +inputClosePin.value === currentAccount.pin
  ) {
    const index = accounts.findIndex(
      acc => acc.username === currentAccount.username
    );
    console.log(index);

    accounts.splice(index, 1);

    containerApp.style.opacity = 0;
  }

  inputCloseUsername.value = inputClosePin.value = '';
});

// Sort button click event handling.
let sorted = false;
btnSort.addEventListener('click', function (e) {
  e.preventDefault();
  displayMovements(currentAccount.movements, !sorted);
  sorted = !sorted;
});
